def countdown(counter)
  counter = 11 
  while counter >= 2
  counter -= 1 
  puts "#{counter} SECOND(S)!"
end
"HAPPY NEW YEAR!"
end

def countdown_with_sleep(counter)
  counter = 11 
  while counter >= 2
  counter -= 1 
  puts "#{counter} SECOND(S)!"
  sleep 1 
end
"HAPPY NEW YEAR!"
end